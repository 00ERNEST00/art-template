const assert = require('assert');
const template = require('../src/index');

const test = (code, data, options, result) => {
    it(code, () => {
        assert.deepEqual(result, template.render(code, data, options));
    });
};


describe('#template', () => {
    test('hello, <%=value%>.', {
        value: 'aui'
    }, {}, 'hello, aui.');
});